x<-1:5; y<-x*x 

# Creating a plot 
plot(x, y, main = "", xlab = "", ylab = "", 
     col.axis = "darkgreen") 

# Calling title() function 
title(main = "Graph ", sub = "Geeksforgeeks article", 
      xlab = "X axis", ylab = "Y axis", 
      cex.main = 4, font.main = 3, col.main = "darkgreen", 
      cex.sub = 2, font.sub = 3, col.sub = "darkgreen", 
      col.lab ="black"
) 